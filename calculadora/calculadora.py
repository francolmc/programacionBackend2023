def add_two_values(first_value: int, second_value: int) -> int:
    return first_value + second_value

def substract_two_values(first_value: int, second_value: int) -> int:
    return first_value - second_value

def division_two_values(first_value: int, second_value: int) -> int:
    if first_value < second_value:
        return -1
    return first_value / second_value

def multiply_two_value(first_value: int, second_value: int) -> int:
    return first_value * second_value